package loop;

public class loop_8 {
    static void main(String[] argst) {
        /*
            TODO
            print numbers from 1 to 5 to the console using 'do-while' loop
            use System.out.println to print values
        */
        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 5);

    }
}
