public class casting {
    static void main() {

        // explicit casting (type casting)
        float f = 3.14f;
        int e = (int) f;
        System.out.println("integer value is: " + e);

        //implicit casting (type promotion)
        int a = 10;
        float b = a;   // implicit casting
        System.out.println(b);



    }
}
